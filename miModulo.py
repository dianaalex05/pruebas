# modulo logico 

def sumar  (n1,n2):
   suma = n1+n2
   return suma

def restar (n1,n2):
     resta = n1-n2
     return resta
    
def multiplicar (n1,n2):
     multiplicacion = n1*n2
     return multiplicacion 
    
def dividir(n1,n2):
    if (n2!=0):
        division = n1/n2
        return division 
    else:
        return 0
         
